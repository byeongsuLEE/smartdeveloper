package java0907;

public class Ex02_자료형 {

	public static void main(String[] args) {
		// 1. 논리형 -> boolean 
		boolean a = true;
		boolean b = false;
		//2. 문자형 (문자 1개) -> char
		char c = 'a';
		//여러개의 문자를 담기 위한 형태 -> String
		String d = "banna";
		//3 정수형.
		int e = 5;
		short f = 3;
		byte g = 1;
		long h = 10;
		
		//4 실수형 
		double i = 3.14;
		double j = 1;
		System.out.println(j);
		float l = 1.2f;   //1.2 는 기본적으로 double 이기떄문에 큰거에서 작은거로 담을경우엔 f로바꿔줘야함
		
	
	}

}
